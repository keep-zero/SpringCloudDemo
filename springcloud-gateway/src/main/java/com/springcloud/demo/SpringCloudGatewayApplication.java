/**
 * @description
 * @auther: lai.guanfu
 * @date: 2019-04-25 11:14
 */
@SpringBootApplication
@EnableEurekaClient
public class SpringCloudGatewayApplication {

    public static void main(String[] args) {
        SpringApplication.run(SpringCloudGatewayApplication.class, args);
    }
}
